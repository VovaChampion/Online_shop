            <p>Batman's secret identity is Bruce Wayne, a wealthy American industrialist. 
                As a child, Bruce witnessed the murder of his parents, 
                Dr. Thomas Wayne and Martha Wayne, which ultimately led him to craft 
                the Batman persona and seek justice against criminals. 
                He resides on the outskirts of Gotham City in his personal residence, Wayne Manor. 
                Wayne averts suspicion by acting the part of a superficial playboy idly
                living off his family's fortune and the profits of Wayne Enterprises, 
                his inherited conglomerate. He supports philanthropic causes through his nonprofit 
                Wayne Foundation, but is more widely known as a celebrity socialite. 
                In public, he frequently appears in the company of high-status women, 
                which encourages tabloid gossip. Although Bruce Wayne leads an active romantic life, 
                his vigilante activities as Batman account for most of his time.</p>

            <p>Various modern stories have portrayed the extravagant, 
                playboy image of Bruce Wayne as a facade.This is in contrast to the post-Crisis 
                Superman, whose Clark Kent persona is the true identity, 
                while the Superman persona is the facade. In Batman Unmasked, 
                a television documentary about the psychology of the character, 
                behavioral scientist Benjamin Karney notes that Batman's personality is 
                driven by Bruce Wayne's inherent humanity; that "Batman, for all its 
                benefits and for all of the time Bruce Wayne devotes to it, is ultimately a tool 
                for Bruce Wayne's efforts to make the world better". 
                Bruce Wayne's principles include the desire to prevent future harm and 
                a vow not to kill. Bruce Wayne believes that our actions define us, 
                we fail for a reason and anything is possible.</p>
